'use strict';

QForms.inherits(Control, Model);

////////////////////////////////////////////////////////////////////////////////////////////////////
function Control(data, parent) {
    var self = this;
    Control.super_.call(self);
    self.name   = data.name;
    self.form   = parent;
    self.data   = data;
    self.parent = parent;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Control.prototype.init = function() {
    var self = this;
};

////////////////////////////////////////////////////////////////////////////////////////////////////
Control.prototype.deinit = function() {
    var self = this;
};